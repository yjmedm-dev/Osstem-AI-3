# 법인별 계정코드 매핑 테이블
ACCOUNT_MAPPINGS: dict[str, dict[str, str]] = {
    "UZ01": _UZ01_ACCOUNT_MAP,
}


def map_columns(df: pd.DataFrame, subsidiary_code: str) -> pd.DataFrame:
    """법인별 컬럼명을 표준 컬럼명으로 변환한다.

    매핑 정의가 없는 법인은 df를 그대로 반환한다.
    """
    mapping = COLUMN_MAPPINGS.get(subsidiary_code.upper())
    if mapping:
        df = df.rename(columns=mapping)
    return df


def map_accounts(df: pd.DataFrame, subsidiary_code: str) -> pd.DataFrame:
    """법인별 현지 계정코드를 한국 표준 신계정코드로 변환한다.

    account_code 컬럼에서 1C 계정코드의 앞 부분(쉼표 이전)을 키로 조회하여
    std_account_code 컬럼이 없는 행에 채워 넣는다.
    매핑 정의가 없는 법인 또는 미매핑 계정은 원본값을 유지한다.
    """
    account_map = ACCOUNT_MAPPINGS.get(subsidiary_code.upper())
    if not account_map or "account_code" not in df.columns:
        return df

    if "std_account_code" not in df.columns:
        df = df.copy()
        df["std_account_code"] = None

    def _lookup(raw_code: str) -> str | None:
        if not isinstance(raw_code, str):
            return None
        key = raw_code.split(",")[0].strip()
        return account_map.get(key) or account_map.get(raw_code.strip())

    mask = df["std_account_code"].isna()
    df.loc[mask, "std_account_code"] = df.loc[mask, "account_code"].map(_lookup)
    return df
